void HAL_GPIO_EXTI_Rising_Callback(uint16_t GPIO_Pin) {
	/*

	 La máquina de estados funciona de la siguiente forma:
	 Siempre que se puse el botón 1 nos "movemos" a la dcha en la máquina de estados (avanzamos al siguiente estado)
	 Cuando se puse el botón 2 nos "moveremos a la izqda en la máquina (vamos al estado anterior del actual)
	 */
	if (GPIO_Pin == GPIO_PIN_13) // Condición: El botón 1 es pulsado
	{
		if (statesLeds == 3) // Si estamos en el S3, pasamos al S0
				{
			statesLeds = 0;
		} else {
			statesLeds++; // Avanzamos al sig estado
		}

	} else if (GPIO_Pin == GPIO_PIN_4) // // Condición: El botón 1 es pulsado
	{
		if (statesLeds == 0) // Si estamos en el S0, pasamos al S3
				{
			statesLeds = 3;
		} else {
			statesLeds--; // Retrocedemos al estado ant.
		}
	}
}
